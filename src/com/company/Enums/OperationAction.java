package com.company.Enums;

/**
 * Defines the action of the operation requested.
 */
public enum OperationAction {

    /**
     * Add, +
     */
    Add,

    /**
     * Subtract, -
     */
    Subtract,

    /**
     * Multiply, *
     */
    Multiply,

    /**
     * Divide /
     */
    Divide,

    /**
     * The operation requested is not supported or invalid.
     */
    Unknown
}
